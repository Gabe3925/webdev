class Animal
  def initialize(name, species, toys)
    @name = name
    @species = species
    @toys = toys
  end

  def name
    return @name
  end

  def name=(name)
    @name = name
    returns @name
  end

  def species
    return @species
  end

  def species=(species)
    @species = species
    returns @species
  end

  def toys
    return @toys
  end

  def toys=(toys)
    @toys = toys
    returns @toys
  end
end
