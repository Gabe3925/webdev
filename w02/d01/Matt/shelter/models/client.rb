class Client
  def initialize(name, age, pets)
    @name = name
    @age = age
    @pets = pets
  end

  def name
    return @name
  end

  def name=(name)
    @name = name
    returns @name
  end

  def age
    return @age
  end

  def age=(age)
    @age = age
    returns @age
  end

  def pets
    return @pets
  end

  def pets=(pets)
    @pets = pets
    returns @pets
  end
end
